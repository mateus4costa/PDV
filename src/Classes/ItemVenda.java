public class ItemVenda {
    private String num_item;
    private String codigo_prdt;
    private int qtd_vendas;
    private float preco;
    private float total_item;

    public String getNum_item() {
        return num_item;
    }

    public void setNum_item(String num_item) {
        this.num_item = num_item;
    }

    public String getCodigo_prdt() {
        return codigo_prdt;
    }

    public void setCodigo_prdt(String codigo_prdt) {
        this.codigo_prdt = codigo_prdt;
    }

    public int getQtd_vendas() {
        return qtd_vendas;
    }

    public void setQtd_vendas(int qtd_vendas) {
        this.qtd_vendas = qtd_vendas;
    }

    public float getPreco() {
        return preco;
    }

    public void setPreco(float preco) {
        this.preco = preco;
    }

    public float getTotal_item() {
        return total_item;
    }

    public void setTotal_item(float total_item) {
        this.total_item = total_item;
    }
    
    
}
